    private void loadComboBoxData() {
        String query = "SELECT * FROM buku";
        ResultSet rs = DB.select(query);
        DefaultComboBoxModel model = new DefaultComboBoxModel();

        try {
            while (rs.next()) {
                String id = rs.getString("ID_BUKU");
                String name = rs.getString("JUDUL_BUKU");
                ComboboxItem item = new ComboboxItem(id, name);
                model.addElement(item);
                
            }
            cbbuku.setModel(model);
            if (cbbuku.getItemCount() > 0) {
                cbbuku.setSelectedIndex(0);
            }
            
           
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        query = "SELECT * FROM anggota";
        rs = DB.select(query);
        DefaultComboBoxModel model2 = new DefaultComboBoxModel();

        try {
            while (rs.next()) {
                String id = rs.getString("ID_ANGGOTA");
                String name = rs.getString("NAMA_ANGGOTA");
                ComboboxItem item = new ComboboxItem(id, name);
                model2.addElement(item);
                
            }
            cbanggota.setModel(model2);
            if (cbanggota.getItemCount() > 0) {
                cbanggota.setSelectedIndex(0);
            }

            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
